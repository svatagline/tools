// COMBINE MULTIPLE EXCEL FILES INTO ONE

const XLSX = require("xlsx");
const fs = require("fs");
const path = require("path");

// Folder containing source Excel files
const SOURCE_DIR = path.join(__dirname, "excels");

// Output file
const OUTPUT_FILE = "combined.xlsx";

// Create a new workbook
const combinedWorkbook = XLSX.utils.book_new();

// Read all Excel files from folder
const files = fs
  .readdirSync(SOURCE_DIR)
  .filter((file) => file.endsWith(".xlsx"));

files.forEach((file, index) => {
  const filePath = path.join(SOURCE_DIR, file);

  // Read source workbook
  const workbook = XLSX.readFile(filePath);

  // Take first sheet from source file
  const sheetName = workbook.SheetNames[0];
  const worksheet = workbook.Sheets[sheetName];

  // Sheet name in combined workbook
  const newSheetName = `Sheet_${index + 1}`;
  // or: path.basename(file, '.xlsx')

  // Append sheet to combined workbook
  XLSX.utils.book_append_sheet(combinedWorkbook, worksheet, newSheetName);

  console.log(`âœ… Added ${file} as ${newSheetName}`);
});

// Write final combined Excel
XLSX.writeFile(combinedWorkbook, OUTPUT_FILE);

